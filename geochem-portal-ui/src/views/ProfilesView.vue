    <h1 class="text-4xl pb-4">Profiles</h1>
    <p>A Profile (capital 'P') is s specification that constrains, extends, combines, or provides guidance or explanation about the usage of other specifications.</p>
    <p>In the context of this geochemistry Standard, Profiles mandate that certain model elements be present in data and that specific vocabularies are used for concept references.</p>
    <p>The two Profiles of this Standard defined to date are:</p>
    <ol>
      <li><a href="http://linked.data.gov.au/def/geochem#_core_profile" target="_blank">CORE Profile <i class="pi pi-external-link"></i></a></li>
      <li><a href="http://linked.data.gov.au/def/geochem#_gsq_profile" target="_blank">GSQ Profile <i class="pi pi-external-link"></i></a></li>
    </ol>
    <p>The Core Profile states the minimum requirements that must be met in order for data to claim conformance to this data model.</p>
    <p>The GSQ Profile mandates which vocabularies must be used to supply codes values in data. It is dependent on the Core Profile.</p>
    <h3 class="text-base">Future Profiles</h3>
    <p>Is is possible that other jurisdictions may define their own Profiles, probably based on the Core Profile, that specify vocabs for use different to, or in addition to, those specified by the GSQ Profile.</p>
    <p>Contact us if you which to make such a profile.</p>
